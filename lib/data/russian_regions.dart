  static List<Map<String, String>> getAll() => regions;

  /// Найти регион по имени
  static Map<String, String>? findByName(String name) {
    try {
      return regions.firstWhere(
        (region) => region['name']?.toLowerCase().contains(name.toLowerCase()) ?? false,
      );
    } catch (e) {
      return null;
    }
  }

  /// Найти регион по ID (коду)
  static Map<String, String>? findById(String id) {
    try {
      return regions.firstWhere(
        (region) => region['id'] == id,
      );
    } catch (e) {
      return null;
    }
  }

  /// Получить регионы по федеральному округу
  static List<Map<String, String>> getByFederalDistrict(String district) {
    return regions.where(
      (region) => region['federalDistrict'] == district,
    ).toList();
  }

  /// Получить список всех федеральных округов
  static List<String> getFederalDistricts() {
    final districts = regions.map((r) => r['federalDistrict']!).toSet();
    return districts.toList()..sort();
  }
